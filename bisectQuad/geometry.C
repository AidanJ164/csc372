struct point
  {
  double x;
  double y;
  };

////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////

double min(double a, double b)
  {
  if (a < b)
    return a;
  return b;
  }

////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////

double max(double a, double b)
  {
  if (a > b)
    return a;
  return b;
  }

////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////

double cross(point a, point b)
  {
  return a.x * b.y - a.y * b.x;
  }

////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////

int on(point a, point b, point c)
  {
  if (((min(a.x, b.x) <= c.x) && (c.x <= max(a.x, b.x))) &&
      ((min(a.y, b.y) <= c.y) && (c.y <= max(a.y, b.y))))
    return 1;
     
  return 0;
  }

////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////

double direction(point a, point b, point c)
  {
  point ab;
  point bc;
  double result;

  ab.x = b.x - a.x;
  ab.y = b.y - a.y;
  bc.x = c.x - b.x;
  bc.y = c.y - b.y;
  result =  cross(ab, bc);
  if (fabs(result) < 1.0e-6)
    result = 0.0;
  return result;
  }

////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////

int turn(point a, point b, point c)
  {
  point ab;
  point bc;

  ab.x = b.x - a.x;
  ab.y = b.y - a.y;
  bc.x = c.x - b.x;
  bc.y = c.y - b.y;
  return (cross(ab, bc) > 0);
  }

////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////

int clockwise(point p[], int n)
  {
  return turn(p[0], p[1], p[2]);
  }

////////////////////////////////////////////////////////////////////
//     Line Segment Instersect                                    //
//     From CLRS text, page 1018 (third edition)                  //
////////////////////////////////////////////////////////////////////

int intersect(point p1, point p2, point p3, point p4)
  {
  double d1;
  double d2;
  double d3;
  double d4;

  d1 = direction(p3, p4, p1);
  d2 = direction(p3, p4, p2);
  d3 = direction(p1, p2, p3);
  d4 = direction(p1, p2, p4);

  if ((((d1 > 0) && (d2 < 0)) || ((d1 < 0) && (d2 > 0))) &&
      (((d3 > 0) && (d4 < 0)) || ((d3 < 0) && (d4 > 0))))
    return 1;

  if ((d1 == 0) && on(p3, p4, p1))
    return 1;

  if ((d2 == 0) && on(p3, p4, p2))
    return 1;

  if ((d3 == 0) && on(p1, p2, p3))
    return 1;

  if ((d4 == 0) && on(p1, p2, p4))
    return 1;

  return 0;
  }

////////////////////////////////////////////////////////////////////
//                   Area of polygon                              //
////////////////////////////////////////////////////////////////////

double area(point p[], int n)
  {
  int i;
  int j;
  double result;

  result = 0;
  for (i = 0; i < n; i++)
    {
    j = (i + 1) % n;
    result += p[i].x * p[j].y;
    result -= p[i].y * p[j].x;
    }
  return fabs(result / 2);
  }

